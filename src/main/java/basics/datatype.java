package basics;

public class datatype {
    public static void main(String[] args) {
        int biriyani = 450;
        float raita = 21.5f;
        String name = "food";
        char initial= 'A';
        double amount= 500.55;
        System.out.println("integer value"+biriyani);
        System.out.println("float value:"+raita);
        System.out.println("string value:"+name);
        System.out.println("character value:"+initial);
        System.out.println("double value:"+amount);

    }
}
